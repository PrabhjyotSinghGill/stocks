/**
 *
 * The 10 data points for social media count for each stock symbol are generated using following logic
 * 1. Create a random number between 10,000 and 20,000, which is the starting point for social media count for a stock symbol
 * 2. Generate a constant factory between -1000 and 1000 which would be always added to the previous day's social media count
 * 3. Along with constant factor generate a random number between -1000 and 1000 for each day and add it to the previous day's social media count.
 * This is done to simulate the scenario where the social media count for a stock symbol is less likely to stay almost same after 10 days
 */
function generateSocialMediaCounts() {
    let constantFactor = Math.floor(Math.random() * 2000) - 1000
    let result = []
    for (let i = 0; i < 10; i++) {
        if (i === 0) {
            result.push(Math.floor(Math.random() * 10000) + 10000)
        } else {
            result.push(
                result[i - 1] +
                    constantFactor +
                    Math.floor(Math.random() * 2000) -
                    1000
            )
        }
    }
    return result
}

