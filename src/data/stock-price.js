/**
 *
 * The 10 data points for closing price for each stock symbol are generated using following logic
 * 1. Create a random number between 100 and 500, which is the starting point for closing price for a stock symbol
 * 2. Generate a constant factory between -10 and 10 which would be always added to the previous day's closing price
 * 3. Along with constant factor generate a random number between -10 and 10 for each day and add it to the previous day's closing price.
 * This is done to simulate the scenario where the closing price for a stock symbol is less likely to stay almost same after 10 days
 */
function generateStockPrices() {
    let constantFactor = Math.floor(Math.random() * 20) - 10
    let result = []
    for (let i = 0; i < 10; i++) {
        if (i === 0) {
            result.push(Math.floor(Math.random() * 400) + 100)
        } else {
            result.push(
                result[i - 1] +
                    constantFactor +
                    Math.floor(Math.random() * 20) -
                    10
            )
        }
    }
    return result
}

